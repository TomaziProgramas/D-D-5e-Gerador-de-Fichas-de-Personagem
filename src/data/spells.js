/**
 * Helpers para filtragem de magias
 */

/** Retorna magias filtradas por nivel */
export function getSpellsByLevel(level) {
  return SPELLS.filter((s) => s.level === level);
}

/** Retorna magias filtradas por classe */
export function getSpellsByClass(classId) {
  return SPELLS.filter((s) => s.classes.includes(classId));
}

/** Retorna magias filtradas por escola de magia */
export function getSpellsBySchool(school) {
  return SPELLS.filter((s) => s.school === school);
}

/** Retorna apenas magias que sao rituais */
export function getRitualSpells() {
  return SPELLS.filter((s) => s.ritual);
}

/** Retorna apenas magias que exigem concentracao */
export function getConcentrationSpells() {
  return SPELLS.filter((s) => s.concentration);
}

/** Busca magias pelo nome (busca parcial, case-insensitive) */
export function searchSpells(query) {
  const lower = query.toLowerCase();
  return SPELLS.filter((s) => s.name.toLowerCase().includes(lower));
}
